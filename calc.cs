using System;
public class Calc
{
    public double Calc_Secti(double a, double b)
    { 
        return a + b;
    }

    public double Calc_Odecti(double a, double b)
    { 
        return a - b;
    }

    public double Calc_Vynasob(double a, double b)
    { 
        return a * b;
    }

    public double Calc_Vydel(double a, double b)
    { 
        return a / b;
    }
}
